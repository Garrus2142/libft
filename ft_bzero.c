#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*s_char;
	int				i;

	s_char = (unsigned char *)s;
	i = 0;
	while (i < (int)n)
	{
		s_char[i] = 0;
		i++;
	}
}

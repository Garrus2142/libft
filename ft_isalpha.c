#include "libft.h"

int		ft_isalpha(int c)
{
	unsigned char	c_c;

	c_c = (unsigned char)c;
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}

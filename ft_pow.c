#include "libft.h"

int	ft_pow(int x, int y)
{
	int	res;
	int	i;

	if (y == 0)
		return (1);
	if (y < 0)
		return (0);
	i = 0;
	res = x;
	while (i < y - 1)
	{
		res *= x;
		i++;
	}
	return (res);
}

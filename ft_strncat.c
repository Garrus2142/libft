#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i_s1;
	size_t	i_s2;

	i_s1 = ft_strlen(s1);
	i_s2 = 0;
	while (i_s2 < n && s2[i_s2] != '\0')
	{
		s1[i_s1] = s2[i_s2];
		i_s1++;
		i_s2++;
	}
	s1[i_s1] = '\0';
	return (s1);
}
